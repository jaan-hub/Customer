package com.example.demo.service;

import java.util.List;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import com.example.demo.exception.InvalidId;
import com.example.demo.exception.InvalidMobileNumber;
import com.example.demo.exception.InvalidName;
import com.example.demo.model.Customer;
import com.example.demo.repository.CustomerRepository;

@Service
public class CustomerServiceImpl implements CustomerService {

	@Autowired
	private CustomerRepository cr;

	@Override
	public void add(Customer customer) {
		// TODO Auto-generated method stub
		Integer id = customer.getId();
		if(id<0 || id == null) {
			throw new InvalidId("Invalid Id");
		}
		
		String name = customer.getName();
		List<Customer> clist = cr.findByName(name);
		if(!clist.isEmpty()) {
			throw new InvalidName("customer name already exist");
		}
		
		String mob = customer.getMob();
		if (mob != null) {
			mob.trim();
		}
		if (mob.startsWith("+91"))
			mob = mob.substring(3);

		if (mob.length() == 10) {
			if (mob.charAt(0) == '0' || mob.charAt(0) == '1' || mob.charAt(0) == '2' || mob.charAt(0) == '3'
					|| mob.charAt(0) == '4' || mob.charAt(0) == '5')
				throw new InvalidMobileNumber("INVALID MOBILE NUMBER");

			for (int i = 0; i < mob.length(); i++) {
				if (!Character.isDigit(mob.charAt(i)))
					throw new InvalidMobileNumber("INVALID MOBILE NUMBER");
			}
			Customer exist = cr.findByMob(mob);
			if (exist != null)
				throw new InvalidMobileNumber("Already Exist mobile Number");

		} else
			throw new InvalidMobileNumber("INVALID MOBILE NUMBER");
		
		List<Customer>list = cr.findAll();
		for(Customer cust : list) {
			if(cust.getId().equals(id)) {
				throw new InvalidId("Id Already Exist"); 
			}
			
			for(Customer custaddress:list) {
				if(custaddress.getAddress().equals(customer.getAddress())) {
					System.out.println("Address Already Exist");
				}
			}
		}
		customer.setId(id);
		cr.save(customer);// Insert
	}

	@Override
	public List<Customer> display() {
		// TODO Auto-generated method stub
		return cr.findAll();// select *from Customer;
	}

	@Override
	public Customer delete(Integer id) {
		// TODO Auto-generated method stub

		// search
		if (cr.findById(id).isPresent()) {
			Customer temp = cr.findById(id).get();
			cr.deleteById(id);// delete
			return temp;
		}

		return null;
	}
 
	@Override
	public void update(Customer customer, Integer id) {
		// TODO Auto-generated method stub

		customer.setId(id);
		cr.save(customer);

	}

	@Override
	public Customer search(Integer id) {
		// TODO Auto-generated method stub
		if (cr.findById(id).isPresent()) {
			Customer temp = cr.findById(id).get();
//			cr.deleteById(id);//delete
			return temp;
		}
		return null;
	}

	@Override
	public void addAll(List<Customer> list) {
		// TODO Auto-generated method stub
		cr.saveAll(list);
	}

	@Override
	public Customer findMob(String mob) {
		// TODO Auto-generated method stub
		return cr.findByMob(mob);
	}

	@Override
	public List<Customer> findByName(String name) {
		// TODO Auto-generated method stub
		return cr.findByName(name);
	}

	@Override
	public List<Customer> findByAddress(String address) {
		// TODO Auto-generated method stub
		return cr.findByAddress(address);
	}

}
